package com.glenwood.network.client.application.domain;

public class Node {

	private String nodeId;

	private String nodeType;

	private int networkId;

	private String parentNodeId;

	private Double realNumberValue;

	private String otherValue;

	private String nodeName;

	private Double maxValue;

	private Double minVal;

	private Integer xValue;

	private Integer yValue;

	private String currentIO;

	public String getCurrentIO(){
		return currentIO;
	}

	public void setCurrentIO(String currentIO){
		this.currentIO = currentIO;
	}

	public String getNodeId() {
		return nodeId;
	}

	public void setNodeId(String string) {
		this.nodeId = string;
	}

	public String getNodeType() {
		return nodeType;
	}

	public void setNodeType(String nodeType) {
		this.nodeType = nodeType;
	}

	public int getNetworkId() {
		return networkId;
	}

	public void setNetworkId(Integer networkId) {
		this.networkId = networkId;
	}

	public String getParentNodeId() {
		return parentNodeId;
	}

	public void setParentNodeId(String parentNodeId) {
		this.parentNodeId = parentNodeId;
	}

	public Double getRealNumberValue() {
		return realNumberValue;
	}

	public void setRealNumberValue(Double realNumberValue) {
		this.realNumberValue = realNumberValue;
	}

	public String getOtherValue() {
		return otherValue;
	}

	public void setOtherValue(String otherValue) {
		this.otherValue = otherValue;
	}

	public String getNodeName() {
		return nodeName;
	}

	public void setNodeName(String nodeName) {
		this.nodeName = nodeName;
	}

	public Double getMaxValue() {
		return maxValue;
	}

	public void setMaxValue(Double maxValue) {
		this.maxValue = maxValue;
	}

	public Double getMinVal() {
		return minVal;
	}

	public void setMinVal(Double minVal) {
		this.minVal = minVal;
	}

	public Integer getxValue() {
		return xValue;
	}

	public void setxValue(Integer xValue) {
		this.xValue = xValue;
	}

	public Integer getyValue() {
		return yValue;
	}

	public void setyValue(Integer yValue) {
		this.yValue = yValue;
	}

}
